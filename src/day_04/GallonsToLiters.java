package day_04;

public class GallonsToLiters {
    public static void main(String[] args) {


        double liters;
        double gallon;


        gallon = 10;

        liters = gallon * 3.78541;

        System.out.println(gallon + " gallons is equal to " + liters + " litters");


    }
}

