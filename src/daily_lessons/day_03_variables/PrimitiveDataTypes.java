package daily_lessons.day_03_variables;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        char a = 60;
        System.out.println(a);

        int sum = 'd' + 'b';
        System.out.println(sum);

        boolean condition = 100<12;
        System.out.println(condition);

    }
}
