package reviews.week_10.earth;

import java.util.ArrayList;

public class Person {
    public   String name;
    public   int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    @Override
    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
